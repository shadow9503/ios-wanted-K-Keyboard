import UIKit

extension UIColor {
    
    static func customBlack() -> UIColor {
        
        return UIColor(red: 0.259, green: 0.267, blue: 0.298, alpha: 1)
    }
    
    static func customLightBlack() -> UIColor {
        
        return UIColor(red: 0.294, green: 0.306, blue: 0.341, alpha: 1)
    }
    
    static func customLightGray() -> UIColor {
        
        return UIColor(red: 0.569, green: 0.573, blue: 0.6, alpha: 1)
    }
    
    static func customPink() -> UIColor {
        
        return UIColor(red: 1, green: 0.255, blue: 0.49, alpha: 1)
        
    }
    
    static func customBlue() -> UIColor {
        
        return UIColor(red: 0.49, green: 0.788, blue: 0.988, alpha: 1)
    }
    
}
